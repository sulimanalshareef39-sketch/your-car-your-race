let currentBrand = "الكل";

function updateCars() {

    const searchValue = searchInput.value.trim().toLowerCase();

    const filtered = {};

    Object.keys(cars).forEach(name => {

        const car = cars[name];

        const matchSearch =
            name.toLowerCase().includes(searchValue);

        const matchBrand =
            currentBrand === "الكل" ||
            car.brand === currentBrand;

        if (matchSearch && matchBrand) {

            filtered[name] = car;

        }

    });

    renderCars(filtered);

}

