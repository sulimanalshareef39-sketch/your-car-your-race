// ==============================
// Calculate Performance Score
// ==============================

function calculateScore(car) {

    const hp = parseInt(car.hp) || 0;
    const torque = parseInt(car.torque) || 0;
    const speed = parseInt(car.speed) || 0;
    const acceleration = parseFloat(car.acceleration) || 0;
    const weight = parseInt(car.weight) || 0;

    let score = 0;

    // القوة

    score += Math.min((hp / 800) * 25, 25);

    // العزم

    score += Math.min((torque / 1500) * 20, 20);

    // السرعة

    score += Math.min((speed / 350) * 15, 15);

    // التسارع

    score += Math.min(
        25,
        Math.max(
            0,
            ((5 - acceleration) / 3) * 25
        )
    );

    // الوزن

    score += Math.min(
        10,
        Math.max(
            0,
            ((3000 - weight) / 1500) * 10
        )
    );

    // نظام الدفع

    switch (car.drivetrain) {

        case "AWD":
            score += 5;
            break;

        case "RWD":
            score += 4;
            break;

        case "FWD":
            score += 3;
            break;

        default:
            score += 2;

    }

    return Math.round(score);

}

// ==============================
// Compare Status
// ==============================

function getStatusClass(value1, value2, reverse = false) {

    if (value1 === value2) {

        return ["draw", "draw"];

    }

    if (!reverse) {

        return value1 > value2

            ? ["win", "lose"]

            : ["lose", "win"];

    }

    return value1 < value2

        ? ["win", "lose"]

        : ["lose", "win"];

}

// ==============================
// Progress Bar
// ==============================

function createBar(value, max) {

    const percent = Math.min(

        (value / max) * 100,

        100

    );

    return `

        <div class="spec-bar">

            <div
                class="spec-fill"
                style="width:${percent}%">
            </div>

        </div>

    `;

}

// ==============================
// Winner Reasons
// ==============================

function getWinnerReasons(carA, carB) {

    const reasons = [];

    if (parseInt(carA.hp) > parseInt(carB.hp))
        reasons.push("💪 قوة أعلى");

    if (parseInt(carA.torque) > parseInt(carB.torque))
        reasons.push("🔥 عزم أعلى");

    if (parseFloat(carA.acceleration) < parseFloat(carB.acceleration))
        reasons.push("🚀 تسارع أفضل");

    if (parseInt(carA.speed) > parseInt(carB.speed))
        reasons.push("🏁 سرعة أعلى");

    if (parseInt(carA.weight) < parseInt(carB.weight))
        reasons.push("⚖️ وزن أخف");

    return reasons.length

        ? reasons.join(" • ")

        : "تقارب كبير بين السيارتين";

}
